package com.techelevator.model;

public class UserDetail {
	
	private String username;
	private String fullName;
	private String phoneNumber;
	private String cityOfResidence;
	private String favTypesOfWhiskeys;
	private String favBrandsOfWhiskeys;
	
	

	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	
	public String getFullName() {
		return fullName;
	}
	public void setFullName(String fullName) {
		this.fullName = fullName;
	}
	public String getPhoneNumber() {
		return phoneNumber;
	}
	public void setPhoneNumber(String phoneNumber) {
		this.phoneNumber = phoneNumber;
	}
	public String getCityOfResidence() {
		return cityOfResidence;
	}
	public void setCityOfResidence(String cityOfResidence) {
		this.cityOfResidence = cityOfResidence;
	}
	public String getFavTypesOfWhiskeys() {
		return favTypesOfWhiskeys;
	}
	public void setFavTypesOfWhiskeys(String favTypesOfWhiskeys) {
		this.favTypesOfWhiskeys = favTypesOfWhiskeys;
	}
	public String getFavBrandsOfWhiskeys() {
		return favBrandsOfWhiskeys;
	}
	public void setFavBrandsOfWhiskeys(String favBrandsOfWhiskeys) {
		this.favBrandsOfWhiskeys = favBrandsOfWhiskeys;
	}
	
	

}
